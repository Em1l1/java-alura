CREATE TABLE clientes (
  DNI VARCHAR(11) NOT NULL,
  NOMBRE VARCHAR(100) NULL,
  DIRECCION VARCHAR(150) NULL,
  BARRIO VARCHAR(50),
  CIUDAD VARCHAR(50),
  ESTADO VARCHAR(10),
  CP VARCHAR(10),
  FECHA_NACIMIENTO DATE,
  EDAD SMALLINT,
  SEXO VARCHAR(1),
  LIMITE_CREDITO FLOAT,
  VOLUMEN_COMPRA FLOAT,
  PRIMERA_COMPRA BIT,
  PRIMARY KEY (DNI)
);

SELECT * FROM clientes;