package first;

public class TipoVariable {
	
	public static void main(String[] args) {
		System.out.println("Hola, Madre estoy programando en Java!!!");
		
		int edad = 28;
		System.out.println(edad);
		
		edad = 47;
		System.out.println(edad);
		
		edad = 46 + 88;
		System.out.println(edad);
		System.out.println("Mi edad es sum " + edad);

		edad = 46 - 88;
		System.out.println("Mi edad es rest " + edad);

		edad = 46 * 88;
		System.out.println("Mi edad es mult " + edad);
		
		edad = 46 / 88;
		System.out.println("Mi edad es div " + edad);
	}

}
